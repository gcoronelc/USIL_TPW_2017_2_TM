package condominio.jeaguer.model;

public class TInmueble {

	private int idtinmueble;
	private String nombre ;
	private int agua;
	private int mante ;
	private double cmante;
	
	
	public TInmueble() {
	}


	public int getIdtinmueble() {
		return idtinmueble;
	}


	public void setIdtinmueble(int idtinmueble) {
		this.idtinmueble = idtinmueble;
	}


	public String getNombre() {
		return nombre;
	}


	public void setNombre(String nombre) {
		this.nombre = nombre;
	}


	public int getAgua() {
		return agua;
	}


	public void setAgua(int agua) {
		this.agua = agua;
	}


	public int getMante() {
		return mante;
	}


	public void setMante(int mante) {
		this.mante = mante;
	}


	public double getCmante() {
		return cmante;
	}


	public void setCmante(double cmante) {
		this.cmante = cmante;
	}
	
	
	
	
}
